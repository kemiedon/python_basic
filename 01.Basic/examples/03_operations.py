# 範例 3: 數值運算

# --- 數值運算 ---
a = 15
b = 4

print("--- 變數設定 ---")
print(f"變數 a = {a}")
print(f"變數 b = {b}")
print("----------------")

# 1. 加法 (+)
print(f"1. 加法 (a + b): {a + b}")

# 2. 減法 (-)
print(f"2. 減法 (a - b): {a - b}")

# 3. 乘法 (*)
print(f"3. 乘法 (a * b): {a * b}")

# 4. 除法 (/) - 結果會是浮點數
print(f"4. 除法 (a / b): {a / b}")

# 5. 地板除法 (//) - 取小於或等於該數的最大整數
print(f"5. 地板除法 (a // b): {a // b}")

# 6. 取餘數 (%)
print(f"6. 取餘數 (a % b): {a % b}")

# 7. 次方 (**)
print(f"7. 次方 (b ** 3): {b ** 3}")
