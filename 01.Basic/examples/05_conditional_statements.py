# Python 條件判斷 (if, elif, else) 的教學範例。
# --- 範例: 根據天氣決定活動 ---
# 讓我們用一個生活化的例子來看看 if-elif-else 的運作方式。

weather = "下雨"  # 你可以試著改成 "晴天" 或 "陰天" 看看結果！

print(f"\n今天天氣是: {weather}")

# 主要條件: 如果今天天氣是晴天
if weather == "晴天":
    print("我就去公園跑步。")

# 次要條件: 否則，如果今天是陰天
elif weather == "陰天":
    print("我就去健身房。")

# 最終備案: 否則（如果既不是晴天也不是陰天）
else:
    print("我就在家看書。")


# --- 範例: 根據年齡判斷票價 ---

# 我們可以設定一個年齡變數，程式會根據這個年齡來決定不同的輸出。
# 你可以試著改變 age 的值，看看輸出的結果有什麼不同！
age = 20

print(f"你的年齡是: {age}")

# 1. 'if' 條件：
#    程式會先檢查第一個 'if' 後面的條件 (age < 6) 是否為 True。
#    如果是，就執行這個區塊內的程式碼，然後整個條件判斷就結束了。
if age < 6:
    price = 0
    print("你是 6 歲以下的兒童，免費入場。")

# 2. 'elif' 條件 (else if 的縮寫):
#    如果上面的 'if' 條件不成立 (False)，程式就會接著檢查這個 'elif' 的條件 (age < 18)。
#    如果是 True，就執行這個區塊的程式碼，然後結束。
#    你可以有多個 'elif' 來檢查不同的情況。
elif age < 18:
    price = 150
    print("你是 6 到 17 歲的學生，票價為 150 元。")

# 3. 'else' 條件:
#    如果上面所有的 'if' 和 'elif' 條件都不成立，
#    程式最後會執行 'else' 這個區塊內的程式碼。
else:
    price = 300
    print("你是成年人，票價為 300 元。")

print(f"最終票價是: {price} 元。")
print("-" * 20)


