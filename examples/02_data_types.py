# 範例 2: 基本資料型別 (int, float, str, bool)

# 1. 整數 (int) - 沒有小數點的數字
age = 30
print(f"變數 'age' 的值是: {age}")
print(f"變數 'age' 的型別是: {type(age)}")
print("-" * 20)

# 2. 浮點數 (float) - 帶有小數點的數字
pi = 3.14159
print(f"變數 'pi' 的值是: {pi}")
print(f"變數 'pi' 的型別是: {type(pi)}")
print("-" * 20)

# 3. 字串 (str) - 用單引號或雙引號包圍的文字
greeting = "Hello, Python!"
print(f"變數 'greeting' 的值是: {greeting}")
print(f"變數 'greeting' 的型別是: {type(greeting)}")
print("-" * 20)

# 4. 布林值 (bool) - 只有 True 或 False 兩種值，常用於邏輯判斷
is_learning = True
print(f"變數 'is_learning' 的值是: {is_learning}")
print(f"變數 'is_learning' 的型別是: {type(is_learning)}")
print("-" * 20)
